function one() {
    let tit = document.getElementById("titulo1");
    let txt = document.getElementById("texto1");
    let grad = document.getElementById("grad1");

    let a = window.getComputedStyle(tit).getPropertyPriority("display");
    let b = window.getComputedStyle(txt).getPropertyPriority("display");
    let c = window.getComputedStyle(grad).getPropertyPriority("background");

    if (a === "none") {
        grad.style.background = "#0000002e";
        txt.style.display = "none";
        tit.style.display = "block";

    } else {
        tit.style.display = "none";
        txt.style.display = "block";
        grad.style.background = "#000000d6";
    }
}

